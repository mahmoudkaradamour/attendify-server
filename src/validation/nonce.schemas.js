/**
 * =============================================================================
 * Attendify Nonce Validation Schemas
 * =============================================================================
 *
 * PURPOSE:
 * -----------------------------------------------------------------------------
 * Defines validation rules for nonce values used in replay protection.
 *
 * -----------------------------------------------------------------------------
 * WHAT IS NONCE?
 * -----------------------------------------------------------------------------
 *
 * Nonce = "Number used once"
 *
 * Guarantees:
 *   ✅ Uniqueness
 *   ✅ Single-use
 *   ✅ Replay resistance
 *
 * -----------------------------------------------------------------------------
 * NONCE FLOW
 * -----------------------------------------------------------------------------
 *
 *       Client Requests Nonce
 *               │
 *               ▼
 *        Server Generates (64 hex)
 *               │
 *               ▼
 *        Client Uses in Request
 *               │
 *               ▼
 *        System Validates + Consumes
 *
 * =============================================================================
 */

const { z } = require("zod");

/* =============================================================================
 * NONCE FORMAT
 * =============================================================================
 *
 * Requirements:
 *   ✅ 64 hex characters (256-bit entropy)
 */

const nonceSchema = z
  .string()
  .length(64, "Nonce must be exactly 64 characters")
  .regex(/^[a-f0-9]+$/i, "Nonce must be hexadecimal");

/* =============================================================================
 * NONCE REQUEST
 * =============================================================================
 *
 * Some endpoints may not require body.
 */

const nonceRequestSchema = z.object({});

/* =============================================================================
 * EXPORTS
 * =============================================================================
 */

module.exports = {

  nonceSchema,

  nonceRequestSchema

};

